<script>
  (() => {
    const containers = document.querySelectorAll(".flipbook-container");
    const fsBtn = document.querySelector(".toggleBtn");
    const fsBtn2 = document.querySelector(".toggleBtn2");
    const fsBtn1 = document.querySelector(".toggleBtn1");
    const exit = document.querySelector(".exit-btn");


    containers.forEach((ctn) => {
      const book = ctn.querySelector(".book");
      if (!book) return;

      const pages = [...book.querySelectorAll(".page")];
      const max = pages.length;
      let c = 0;

      const set = (next) => {
        c = Math.max(0, Math.min(max, next));
        book.style.setProperty("--c", c);
        book.dataset.c = c;
      };

      pages.forEach((p, i) => {
        p.style.setProperty("--i", i);
        p.querySelector(".front")?.addEventListener("click", () => set(i + 1), { passive: true });
        p.querySelector(".back")?.addEventListener("click", () => set(i), { passive: true });
        p.addEventListener(
          "click",
          (e) => {
            if (!e.target.closest(".front, .back")) set(i + 1);
          },
          { passive: true }
        );
      });

      ctn.querySelector(".prev")?.addEventListener("click", () => set(c - 1));
      ctn.querySelector(".next")?.addEventListener("click", () => set(c + 1));

      // --- Fullscreen logic (locked until Exit clicked) ---
      const toggle = (v) => {
        const full = typeof v === "boolean" ? v : !ctn.classList.contains("fullscreen");
        ctn.classList.toggle("fullscreen", full);
        ctn.classList.toggle("small", !full);
        if (exit) exit.style.display = full ? "block" : "none";

        // Prevent body scroll
        document.body.style.overflow = full ? "hidden" : "";
      };

      fsBtn?.addEventListener("click", () => toggle(true));
      fsBtn2?.addEventListener("click", () => toggle(true));
      fsBtn1?.addEventListener("click", () => toggle(true));
      exit?.addEventListener("click", () => toggle(false));

      // Disable ESC or click-outside exits
      document.addEventListener("keydown", (e) => {
        if (e.key === "Escape" && ctn.classList.contains("fullscreen")) {
          e.preventDefault();
          e.stopPropagation();
        }
      });
      document.addEventListener("click", (e) => {
        if (
          ctn.classList.contains("fullscreen") &&
          !ctn.contains(e.target) &&
          !e.target.closest(".exit-btn, .toggleBtn")
        ) {
          e.stopPropagation();
          e.preventDefault();
        }
      });

      set(0);
    });
  })();


